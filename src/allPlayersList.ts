export const ALL_RICH_EXTRA_PLAYERS: Omit<Player, 'id'>[] = COMPRESSED_PLAYER_RECORDS.map(record => {
  const [name, teamsStr] = record.split('|');
  const teams = teamsStr.split(',').map(t => t.trim());
  
  const isForeign = !ISRAELI_PLAYERS.has(name);
  const isNba = NBA_PLAYERS.has(name);
  const isBig = BIGS_PLAYERS.has(name);
  const isEuro = EURO_PLAYERS.has(name);
  const isKattash = KATTASH_PLAYERS.has(name);
  const isGershon = GERSHON_PLAYERS.has(name);
  const isNational = ISRAELI_NATIONAL.has(name);
  const hasChampionship = CHAMPIONS.has(name);
  const hasCup = CUP_WINNERS.has(name);
  const hashPlayed3Teams = THREE_PLUS_TEAMS.has(name) || teams.length >= 3;

  // Derive popularity/rarity realistically or based on name length/hash
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = (hash << 5) - hash + name.charCodeAt(i);
    hash |= 0;
  }
  const popularity = Math.abs(hash) % 45 + (isNba ? 45 : isEuro ? 35 : 20);

  return {
    name,
    isForeign,
    teams,
    nba: isNba,
    drafted: isNba && (Math.abs(hash) % 2 === 0),
    championship: hasChampionship,
    cup: hasCup,
    nationalTeam: isNational,
    over205: isBig && (Math.abs(hash) % 3 !== 0),
    position: isBig ? 'big' : 'guard',
    scored1500: Math.abs(hash) % 4 === 0 || isNba || isEuro,
    euroleague: isEuro,
    coachedByKattash: isKattash,
    coachedByGershon: isGershon,
    finalsMVP: hasChampionship && (Math.abs(hash) % 7 === 0),
    played3PlusTeams: hashPlayed3Teams,
    popularity: Math.min(Math.max(popularity, 5), 98)
  };
});
